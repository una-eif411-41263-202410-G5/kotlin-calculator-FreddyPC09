package design.patterns.design.patterns.calculadora

class Calculadora {
    fun sumar(sum: Int, suma: Int): Int {
        return sum + suma
    }

    fun multiplicar(fact: Int, factor: Int) : Int {
        return fact * factor
    }

    fun dividir(dividendo: Int, divisor: Int) : Int {
        return dividendo / divisor
    }
}